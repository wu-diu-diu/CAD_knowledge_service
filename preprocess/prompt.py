def lighting_layout_prompt(lamp_count, switch_count, matrix_col, matrix_row, matrix):
    return f"""
# 角色设定
你是一个专业的建筑智能化布点算法专家。你具备极强的二维空间解析能力、逻辑推理能力，并且严格遵守设计规范。

# 任务目标
我将提供一个二维离散化矩阵，行数为{matrix_row}，列数为{matrix_col}，代表一个房间的平面图。请你根据特定规则，计算并在矩阵中合理布置“开关”和“灯具”，最后输出它们的坐标（行号, 列号），索引从 0 开始。

# 矩阵图例说明
- `0`：障碍物或墙体外部，绝对不可布置任何设备。
- `1`：可用空间，可以布置灯具或开关。
- `2`：门的位置。

# 布置核心规则
## 1. 开关布置规则（Switch）
- 数量要求：请布置 {switch_count} 个开关。
- 位置要求：
  1. 开关必须布置在数值为 `1` 的坐标上。
  2. 开关不可以布置在数值为 `2` 的坐标上（即门的位置）。
  3. 开关必须处于矩阵最边缘（行号为 0 或最大行号，或列号为 0 或最大列号；或紧贴不可用区域边界）。

## 2. 灯具布置规则（Light）
- 位置要求：必须布置在数值为 `1` 的坐标上。
- 形态要求：灯具排列尽可能横平竖直（网格化、行列对齐）。
- 分布要求：灯具之间尽量均匀，覆盖可用空间，避免扎堆。
- 数量要求：请布置 {lamp_count} 个灯具。

# 输入离散化矩阵
```json
{matrix}
```

# 输出格式
```json
{{
  "switches": [[row1, col1], [row2, col2]],
  "lights": [[row1, col1], [row2, col2], [row3, col3]]
}}
```

严格按照输出格式输出，不要添加任何额外解释文本。
"""


def lighting_type_count_prompt(
    lamp_options,
    room_name,
    room_area_m2,
    target_lux,
    lamp_flux_lm_map
):
    return f"""
# 角色设定
你是一个专业的建筑照明设计助手，负责在候选灯具中选择最合适的灯具类型，并根据照度公式计算灯具数量。

# 任务目标
给定房间名称、房间面积、照度要求和候选灯具类型作为输入：
- 根据房间名称，从候选灯具类型中选择最合理的灯具类型；
- 根据选择的灯具类型的光通量，房间面积，房间照度要求，根据下方公式计算灯具数量；

# 已知输入
- 房间名称: {room_name}
- 房间面积: {room_area_m2} m²
- 房间照度要求: {target_lux} lx
- 可选灯具类型: {lamp_options}
- 灯具光通量数据: {lamp_flux_lm_map}

# 计算规则
使用公式:
N = ceil((E × A) / (UF × MF × Φ))

其中:
- E: 照度要求(lx)
- A: 房间面积(m²)
- Φ: 单灯光通量(lm)
- UF: 使用系数，固定取 0.6
- MF: 维护系数，固定取 0.8

# 要求
灯具数量必须为整数

# 输出格式
```json
{{
  "lamp_type": "灯具类型",
  "lamp_count": 6,
}}
```

严格按照 JSON 输出，不要输出任何解释文本。
"""
